package com.lunengsoft.bean;

public class PdevUtilBean {
   public String id;  //设备id
   public String tname;  //设备名称
   public String tid;  //所属终端id
   public String type;  //设备类型
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getTname() {
	return tname;
}
public void setTname(String tname) {
	this.tname = tname;
}
public String getTid() {
	return tid;
}
public void setTid(String tid) {
	this.tid = tid;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
   
}
